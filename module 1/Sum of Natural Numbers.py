n=int(input("Enter a number: ")) #initializing variable

sum=0 #initializing sum variable
i=1 #initializing i variable
while i<=n: #condition
    sum+=i #adding i to sum
    i+=1 #incrementing the value of i
    print(sum) #printing the value of sum
    print("\nSum of first",n,"natural numbers =",sum) #printing the sum of first n natural numbers10